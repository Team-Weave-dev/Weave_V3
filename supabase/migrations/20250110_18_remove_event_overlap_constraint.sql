-- =====================================================
-- 이벤트 시간 중복 제약 조건 제거
-- =====================================================
-- 설명: 사용자가 같은 시간에 여러 일정을 가질 수 있도록 제약 조건 제거
-- 작성일: 2025-01-10
-- 이유:
--   1. 같은 시간에 여러 이벤트(회의 + 작업)가 필요할 수 있음
--   2. 하루 종일 이벤트와 특정 시간 이벤트가 겹칠 수 있음
--   3. 애플리케이션 레벨에서 경고만 표시하면 충분
-- 의존성: 20250107_05_events.sql

-- 1. 기존 제약 조건 제거
ALTER TABLE public.events
  DROP CONSTRAINT IF EXISTS no_overlapping_events;

-- 완료 메시지
DO $$
BEGIN
  RAISE NOTICE '✅ 이벤트 시간 중복 제약 조건 제거 완료 - 사용자가 같은 시간에 여러 일정을 가질 수 있습니다';
END $$;
